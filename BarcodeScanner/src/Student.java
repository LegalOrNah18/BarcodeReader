
public class Student {
	
	
	public String name;
	public String last_name;
	public int id;
	public int mobile_num;
	public Society society;
	public String course;
	
	public int getID() {
		return id;
	}
	
	public int mobile_num() {
		return mobile_num;
	}
	
	public String getName() {
		return name;
	}
	
	public String getLName() {
		return last_name;
	}
	
	public Society getSociety() {
		return society;
	}
	
	public String getCourse() {
		return course;
	}
	
	
}
